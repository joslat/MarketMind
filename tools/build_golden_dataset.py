"""
Golden-dataset builder (v2) — reads data/*.json (the single source of truth) and
computes per-company ABNORMAL returns using each company's LOCAL benchmark
(§6.5: compare a stock to an index trading in the SAME session, to cut timezone noise).

ground-truth sign = sign(abnormal return) over the D-1 .. D+1 window
  ret      = close[D+1] / close[D-1] - 1                 (company, in its listing's session)
  bench    = close[D+1] / close[D-1] - 1                 (its benchmark index, same window)
  abnormal = ret - bench

Outputs:
  tools/golden.json                    {event_id: {ticker: {ret, bench, abn, benchmark}}}
  cypher/03-fill-impactrecords.cypher  fills EVERY event's ImpactRecords (all events, not just anchors)

Run:  pip install yfinance pandas
      python tools/build_golden_dataset.py

NOTE: golden.json is keyed by the long event ids in data/events.json (the single source
of truth). tools/export_cascades.py and the parity tools read it for ground-truth actuals;
engine.py (v2) reads data/*.json directly. This builder is the canonical ground-truth source.
"""
import json
import os
import sys
import datetime as dt

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
DATA = os.path.join(ROOT, "data")


def load(name):
    with open(os.path.join(DATA, name), "r", encoding="utf-8") as f:
        return json.load(f)


companies = load("companies.json")["companies"]
events = load("events.json")["events"]

# tradable price tickers + each one's benchmark
PT = {c["ticker"]: c["priceTicker"] for c in companies if c.get("tradable") and c.get("priceTicker")}
BM = {c["ticker"]: c.get("benchmark") for c in companies if c.get("tradable") and c.get("priceTicker")}
benchmarks = sorted({b for b in BM.values() if b})
symbols = sorted(set(PT.values()) | set(benchmarks))

# download window spans all event dates (incl. 2025) with a buffer for the D-1/D+1 lookups
ev_dates = [dt.date.fromisoformat(e["date"]) for e in events]
start = (min(ev_dates) - dt.timedelta(days=10)).isoformat()
end = (max(ev_dates) + dt.timedelta(days=12)).isoformat()

try:
    import yfinance as yf
except ImportError:
    sys.exit("yfinance not installed — run:  pip install yfinance pandas")

print(f"Downloading {len(symbols)} symbols {start}..{end} ...")
raw = yf.download(symbols, start=start, end=end, auto_adjust=True, progress=False)
close = raw["Close"] if "Close" in raw else raw


def series(sym):
    if not sym:
        return None
    try:
        s = close[sym].dropna() if len(symbols) > 1 else close.dropna()
        s.index = [d.date() for d in s.index]
        return s if len(s) else None
    except Exception:
        return None


def on_or_before(s, d):
    cand = [x for x in s.index if x <= d]
    return s[cand[-1]] if cand else None


def on_or_after(s, d):
    cand = [x for x in s.index if x >= d]
    return s[cand[0]] if cand else None


def window_return(s, date_str):
    d = dt.date.fromisoformat(date_str)
    pre = on_or_before(s, d - dt.timedelta(days=1))
    post = on_or_after(s, d + dt.timedelta(days=1))
    if pre is None or post is None or float(pre) == 0.0:
        return None
    return float(post / pre - 1.0)


# cache series so we fetch each symbol once
_cache = {}
def get(sym):
    if sym not in _cache:
        _cache[sym] = series(sym)
    return _cache[sym]


golden = {}
for ev in events:
    eid, d = ev["id"], ev["date"]
    row = {}
    for tk, sym in PT.items():
        s = get(sym)
        if s is None:
            continue
        r = window_return(s, d)
        if r is None:
            continue  # no price history in window (e.g. company listed after the event)
        bsym = BM.get(tk)
        bs = get(bsym) if bsym else None
        br = window_return(bs, d) if bs is not None else None
        if br is None:
            # honest: with no benchmark return we cannot compute a true ABNORMAL return.
            # Treating raw return as abnormal would smuggle market beta into the ground
            # truth (§6.5: abnormal = ret - bench). Skip the observation and warn instead.
            print(f"    warn: {eid:34} {tk}: no benchmark return ({bsym or 'no benchmark'}) — skipped (cannot compute abnormal)")
            continue
        row[tk] = {"ret": round(r, 4), "bench": round(br, 4),
                   "abn": round(r - br, 4), "benchmark": bsym}
    golden[eid] = row
    print(f"  {eid:34} {d}  scored={len(row):3}/{len(PT)}")

with open(os.path.join(HERE, "golden.json"), "w", encoding="utf-8") as f:
    json.dump(golden, f, indent=1)
print(f"wrote tools/golden.json  ({sum(len(r) for r in golden.values())} company-event observations)")

# ---- emit cypher/03: fill ImpactRecords for EVERY event (matched by event id + ticker) ----
lines = ["// AUTO-GENERATED by build_golden_dataset.py — real abnormal returns (local benchmarks)",
         "// Run after cypher/01-marketmind-seed.cypher. Covers all events in data/events.json."]
for ev in events:
    for tk, v in golden.get(ev["id"], {}).items():
        lines.append(
            f"MATCH (e:NewsEvent {{id:'{ev['id']}'}})-[:HAS_RECORD]->(ir:ImpactRecord)-[:FOR_COMPANY]->(c:Company {{ticker:'{tk}'}}) "
            f"SET ir.actualReturnPct={v['ret']*100:.2f}, ir.abnormalReturnPct={v['abn']*100:.2f}, "
            f"ir.benchmark='{v['benchmark']}', ir.filled=true;")
with open(os.path.join(ROOT, "cypher", "03-fill-impactrecords.cypher"), "w", encoding="utf-8", newline="\n") as f:
    f.write("\n".join(lines) + "\n")
print(f"wrote cypher/03-fill-impactrecords.cypher  ({len(lines)-2} SET statements)")
